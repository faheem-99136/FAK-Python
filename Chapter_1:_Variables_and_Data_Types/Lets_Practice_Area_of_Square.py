# PRACTICE: AREA OF A SQUARE
# -------------------------
# This program calculates the area of a square based on user input.

# Ask the user to enter the length of one side of the square
side = float(input("Enter length of 1 side of square: "))  # Converts input to float for decimal support

# Calculate the area (side * side) and display the result
print("Area of the square is:", side * side)  # Or we can write it as area = side*side and then print(side)

# You can try different values to see how the area changes!