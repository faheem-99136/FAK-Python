# The print() function displays output on the screen.
# Anything inside the parentheses and quotes will be printed.

print("Hello world")  # Prints a greeting message

# You can print your name or any text:
print("Faheem Alam Khan")  # Prints your name

# You can print numbers as strings or perform calculations:
print("23")           # Prints the string '23'
print(24 + 767)       # Prints the result of the addition: 791

# You can print special characters and symbols as well:
print("!, @, #, $, %, ^, &, *, etc")  # Prints special characters

# The print() function is very flexible and can display text, numbers, and symbols.
# Try changing the text or numbers to see different outputs!