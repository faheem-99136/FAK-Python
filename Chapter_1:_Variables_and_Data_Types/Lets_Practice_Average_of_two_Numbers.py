# PRACTICE: AVERAGE OF TWO NUMBERS
# --------------------------------
# This program calculates the average of two floating-point numbers entered by the user.

# Get first number as float
a = float(input("Enter first number: "))

# Get second number as float
b = float(input("Enter second number: "))

# Calculate the average
average = (a + b) / 2

# Display the result
print("The average of the two numbers you entered is", average)

# Try entering different numbers to see how the average changes!