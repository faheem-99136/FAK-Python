# COMMENTS IN PYTHON
# ------------------
# Comments are parts of a program that are ignored by the Python interpreter.
# They are used to explain code, make notes, or temporarily disable code during development.
# Multi-line comments in Python:
# You can use triple quotes (three double quotes or three single quotes) to write comments that span multiple lines.
# These are actually multi-line strings, but if not assigned to a variable or used as a docstring, they are ignored by the interpreter.
