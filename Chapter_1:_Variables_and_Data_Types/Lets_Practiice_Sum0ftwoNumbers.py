# PRACTICE: SUM OF TWO NUMBERS
# ---------------------------
# This program asks the user to input two numbers and prints their sum.

a = int(input("Enter first number: "))   # Get first number as integer
b = int(input("Enter second number: "))  # Get second number as integer

# Calculate the sum of the two numbers
sum = a + b

# Display the result
print("The sum of two numbers you entered is:", sum)

