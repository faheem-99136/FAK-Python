# PRACTICE: TRUE OR FALSE COMPARISON
# ----------------------------------
# This program asks the user to input two integers, a and b.
# It prints True if 'a' is greater than or equal to 'b', otherwise it prints False.

a = int(input("Enter first number: "))   # Get first integer from user
b = int(input("Enter second number: "))  # Get second integer from user

# Compare the two numbers and print the result
print(a >= b)  # Prints True if a >= b, else False

