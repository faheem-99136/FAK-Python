# RULES FOR IDENTIFIERS IN PYTHON
# ------------------------------
# Identifiers are the names given to variables, functions, classes, etc.
# There are specific rules for naming identifiers in Python:

# 1. Identifiers can only contain letters (a-z, A-Z), digits (0-9), and underscores (_).
# 2. Identifiers cannot start with a digit. They must start with a letter or underscore.
# 3. Identifiers are case sensitive. For example, 'Name' and 'name' are different.
# 4. Keywords (reserved words) cannot be used as identifiers.
# 5. Special characters like !, @, #, $, %, etc. are not allowed in identifiers.

# Examples of valid identifiers:
# my_variable
# age2
# _hidden
# Name

# Examples of invalid identifiers:
# 2age      # Starts with a digit
# my-variable # Contains a hyphen
# for        # 'for' is a keyword

# Always follow these rules to avoid errors in your Python programs!