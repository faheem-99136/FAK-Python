"""
=================
Tuple Methods
=================
This script demonstrates two important tuple methods: index() and count().
"""

# Example tuple
my_tuple = (2, 1, 4, 8, 7, 5, 3, 6, 9)

# a) index()
# Finds the index of the first occurrence of an element in the tuple
print("Index of 4:", my_tuple.index(4))  # Output: 2

# b) count()
# Counts the total occurrences of an element in the tuple
print("Count of 2:", my_tuple.count(2))  # Output: 1